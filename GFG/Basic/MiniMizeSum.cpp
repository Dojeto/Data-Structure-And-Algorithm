class Solution{
    public:
    long long int minValue(int a[], int b[], int n)
    {
        int i;
        long long int temp=0;
        sort(a,a+n);
        sort(b,b+n,greater<int>());
        for(i=0;i<n;i++)
        {
            temp = temp + a[i] * b[i];
        }
        return temp;
    }
};

